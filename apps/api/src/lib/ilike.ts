// Подстрочный ILIKE-паттерн с эскейпом спецсимволов LIKE. Бэкслэш — первым:
// он сам escape-символ (юзерский трейлинг-«\» иначе превращает финальный «%»
// в литерал и тихо ломает поиск), «%»/«_» без эскейпа работают wildcards'ами.
export function ilikeContains(term: string): string {
  return "%" + term.replace(/[\\%_]/g, "\\$&") + "%";
}
