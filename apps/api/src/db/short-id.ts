// 12-hex короткий id (вместо UUID-36) для PK во всех таблицах. Достаточно для нашей
// шкалы: 16^12 ≈ 2.8 × 10^14 коллизий-пространство, при ~10^6 записей вероятность
// коллизии ничтожна. URL-safe (только [0-9a-f]). На стороне DB — обычный text.
export function shortId(): string {
  return crypto.randomUUID().replace(/-/g, "").slice(0, 12);
}
